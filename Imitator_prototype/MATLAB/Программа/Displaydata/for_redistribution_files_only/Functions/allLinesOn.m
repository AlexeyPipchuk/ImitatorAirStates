% Делаем все линии видными
function setLineson(inform)
    set(inform.ch1.coordinate,'Visible','on');
    set(inform.ch1.speed,'Visible','on');
    set(inform.ch1.acceleration,'Visible','on');
    
    set(inform.ch2.coordinate,'Visible','on');
    set(inform.ch2.speed,'Visible','on');
    set(inform.ch2.acceleration,'Visible','on');
    
    set(inform.ch3.coordinate,'Visible','on');
    set(inform.ch3.speed,'Visible','on');
    set(inform.ch3.acceleration,'Visible','on');

